<?php 


Class Calculation{

    function add($a, $b){
        echo 'Add = '. ($a + $b) .'<br>';
    }

    function sub($a, $b){
        echo 'Sub = '. ($a - $b) .'<br>'  ;
    }

    function mul($a, $b){
        echo 'Multi = '. ($a * $b) .'<br>'  ;
    }

    function div($a, $b){
        echo 'Div = '. ($a / $b) .'<br>'  ;
    }

}